// Complete 44-card oracle deck

export const getRandomCard = () => {
  return oracleCards[Math.floor(Math.random() * oracleCards.length)];
};

export const getCardById = (id) => {
  return oracleCards.find(card => card.id === id);
};

export const drawCards = (count = 1) => {
  const shuffled = [...oracleCards].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const searchCards = (query) => {
  const lowerQuery = query.toLowerCase();
  return oracleCards.filter(card => 
    card.name.toLowerCase().includes(lowerQuery) ||
    card.keywords.some(keyword => keyword.includes(lowerQuery)) ||
    card.theme.toLowerCase().includes(lowerQuery)
  );
};
